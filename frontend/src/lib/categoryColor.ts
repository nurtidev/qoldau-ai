/**
 * Тактичный цветокод категорий услуг (design wave 2 — глубина каталога;
 * ребрендинг под официальную палитру АО «Байтерек» — только зелёный + тан/
 * золото, без чужих hue).
 *
 * Каталог услуг раньше был «серой массой» одинаковых бежево-белых карточек.
 * Даём сдержанный цветовой акцент по направлению (category), не заливку:
 * левый кант карточки + мягкий бейдж. Палитра — оттенки зелёного (разной
 * светлоты/тона) + тан-золото для «Гарантии»/«Гранты» — НЕ неон и НЕ радуга,
 * зеркалит значения из index.css :root (--cat-*), но хранится тут как hex,
 * т.к. JS считает alpha-подмешивание для мягкого фона бейджа.
 *
 * Каждый цвет проверен на контраст ≥4.5:1 (WCAG AA) как текст бейджа поверх
 * своего мягкого фона (categorySoftBg, alpha 0.06) — см. таблицу контраста
 * в отчёте ребрендинга. Левый кант карточки декоративный, контраст для него
 * не требуется.
 */

const CATEGORY_COLORS: Record<string, string> = {
  'Финансирование': '#007A40', // primary green
  'Кредит':         '#00572E', // deep green
  'Лизинг':         '#085E2C', // deep green (чуть светлее «Кредита»)
  'Гарантии':       '#6F522A', // dark tan
  'Субсидии':       '#387557', // sage green
  'Гранты':         '#796834', // gold-brown text
  'Экспорт':        '#176D62', // teal-green (внутри зелёной семьи)
  'Инвестиции':     '#033A23', // very deep green
  'Агросектор':     '#257E43', // lighter green
}

const DEFAULT_COLOR = '#007A40' // == var(--color-primary)

function hexToRgb(hex: string): [number, number, number] {
  const h = hex.replace('#', '')
  const n = parseInt(h, 16)
  return [(n >> 16) & 255, (n >> 8) & 255, n & 255]
}

/** Глубокий приглушённый цвет категории. Используй как акцент (кант, точка), не заливку. */
export function categoryColor(category?: string | null): string {
  if (!category) return DEFAULT_COLOR
  return CATEGORY_COLORS[category] ?? DEFAULT_COLOR
}

/**
 * Мягкий тонированный фон под бейдж/иконку — цвет категории поверх белого на
 * заданной прозрачности. Дефолт 0.06 подобран так, чтобы текст цвета
 * categoryColor() поверх этого фона держал контраст ≥4.5:1 (WCAG AA) для
 * всей палитры категорий, включая самую светлую («Агросектор», ratio ~4.68).
 */
export function categorySoftBg(category?: string | null, alpha = 0.06): string {
  const [r, g, b] = hexToRgb(categoryColor(category))
  return `rgba(${r}, ${g}, ${b}, ${alpha})`
}
