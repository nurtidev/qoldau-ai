-- 016_news: новостной модуль портала (editorial-раздел /news).
-- Контент переносится из захардкоженного массива NewsPage.tsx в БД, чтобы
-- редакцией новостей можно было управлять из админ-кабинета без правки кода.

CREATE TABLE news (
    id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    title         VARCHAR(500) NOT NULL,
    lead          TEXT,                     -- лид: 1–3 предложения
    body          TEXT,                     -- тело в markdown-подобном формате
    rubric        VARCHAR(60),              -- Новости | Программы | Истории успеха | СМИ о нас
    source        VARCHAR(255),             -- источник публикации
    source_url    VARCHAR(1000),
    image_url     VARCHAR(1000),            -- опциональная обложка (иначе градиент по рубрике)
    published_at  DATE,
    is_featured   BOOLEAN NOT NULL DEFAULT FALSE,
    sort_order    INT NOT NULL DEFAULT 0,
    created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_news_published ON news(published_at DESC);

