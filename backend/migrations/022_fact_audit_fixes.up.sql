-- ─── 9. Остаточные строки «БРК-Лизинг» в JSONB услуг ─────────────────────────
-- form_schema (003) и eligibility_rules (004) хранят «БРК-Лизинг» в подписях
-- полей, чекбоксах согласий, опциях банков-партнёров и detail-строках правил.
-- Заменяем на «Фонд развития промышленности» текстовым replace по ::text.
-- Порядок важен и делает замену идемпотентной:
--   1) 'АО «БРК-Лизинг»'  → полная форма (на случай появления с «АО»);
--   2) 'БРК-Лизингу' (дат. падеж, чекбокс согласия) → 'Фонду развития
--      промышленности' — иначе «промышленностиу» ломает грамматику;
--   3) остаточный 'БРК-Лизинг' (им. падеж) → 'Фонд развития промышленности'.
-- Шаг 2 обязан идти до шага 3, т.к. 'БРК-Лизинг' — подстрока 'БРК-Лизингу'.
UPDATE services SET form_schema = replace(replace(replace(
    form_schema::text,
    'АО «БРК-Лизинг»', 'Фонд развития промышленности'),
    'БРК-Лизингу',     'Фонду развития промышленности'),
    'БРК-Лизинг',      'Фонд развития промышленности')::jsonb
WHERE form_schema::text LIKE '%БРК-Лизинг%';

UPDATE services SET eligibility_rules = replace(replace(replace(
    eligibility_rules::text,
    'АО «БРК-Лизинг»', 'Фонд развития промышленности'),
    'БРК-Лизингу',     'Фонду развития промышленности'),
    'БРК-Лизинг',      'Фонд развития промышленности')::jsonb
WHERE eligibility_rules::text LIKE '%БРК-Лизинг%';
