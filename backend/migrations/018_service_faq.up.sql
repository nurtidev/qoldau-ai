-- 018_service_faq: блок «Вопросы и ответы» (FAQ) на детальной странице услуги.
--
-- Kaspi-подобный аккордеон с голосованием «был ли ответ полезен» (up/down).
-- service_id IS NULL — общий вопрос портала, показывается на всех услугах;
-- service_id NOT NULL — вопрос, привязанный к конкретной услуге. Контент
-- управляется из админ-кабинета (таб «FAQ»), голоса инкрементятся публично.

CREATE TABLE service_faq (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    service_id  UUID REFERENCES services(id) ON DELETE CASCADE,  -- NULL = общий вопрос
    question    TEXT NOT NULL,
    answer      TEXT NOT NULL,                                   -- markdown-текст
    sort_order  INT  NOT NULL DEFAULT 0,
    up_votes    INT  NOT NULL DEFAULT 0,
    down_votes  INT  NOT NULL DEFAULT 0,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_service_faq_service ON service_faq(service_id);

